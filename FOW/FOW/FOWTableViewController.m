#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    // Return the number of sections.
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    // Return the number of rows in the section.
    if (!_arrayViews) {
        return 0;
    }
    return [_arrayViews count];
}

- (CGFloat)tableView:(UITableView *)tableView heightForRowAtIndexPath:(NSIndexPath *)indexPath {
    FOWViewCell *cell = (FOWViewCell*)[_arrayViews objectAtIndex:indexPath.row];
    [cell caliculateHeight];
    if (indexPath.row == ([_arrayViews count] - 1)) {
        return ([cell getHeight] + 17.0);
    }
    return [cell getHeight];
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"Cell";
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier forIndexPath:indexPath];
    if (cell == nil) {
        cell = [[UITableViewCell alloc] initWithStyle:UITableViewCellStyleDefault reuseIdentifier:CellIdentifier];
        cell.selectionStyle = UITableViewCellSelectionStyleNone;
        FOWViewCell *customCell = (FOWViewCell*)[_arrayViews objectAtIndex:indexPath.row];
        [cell addSubview:customCell];
    }
    
    return cell;
}

